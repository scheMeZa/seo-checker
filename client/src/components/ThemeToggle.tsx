function getInitialTheme() {
  if (typeof window === 'undefined') return 'light';
  if (localStorage.theme === 'dark') return 'dark';
  return 'light';
}

export default function ThemeToggle() {
  const [theme, setTheme] = useState(getInitialTheme());

  useEffect(() => {
    if (theme === 'dark') {
      document.documentElement.classList.add('dark');
      localStorage.theme = 'dark';
    } else {
      document.documentElement.classList.remove('dark');
      localStorage.theme = 'light';
    }
  }, [theme]);

  const toggleTheme = () => {
    setTheme(theme === 'dark' ? 'light' : 'dark');
  };

  return (
    <button
      className="fixed top-6 right-8 z-50 p-2 md:p-3 rounded-full bg-gradient-to-b from-[color:var(--color-primary)] to-blue-600 dark:from-blue-400 dark:to-blue-500 text-white shadow-2xl hover:shadow-lg active:translate-y-0.5 active:shadow-inner hover:bg-[color:var(--color-primary)]/90 dark:hover:bg-blue-300 focus:outline-none focus:ring-4 focus:ring-primary/30 dark:focus:ring-blue-300/40 transition-all duration-150 flex items-center justify-center cursor-pointer select-none"
      aria-label="Toggle dark mode"
      type="button"
      onClick={toggleTheme}
    >
      {theme === 'dark' ? SunIcon : MoonIcon}
    </button>
  );
} 